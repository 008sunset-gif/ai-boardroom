// 役職の定義。配列で管理し、将来の追加・並べ替えに耐えるようにする。
// 現在は CEO（議長・固定参加）＋ 6役職。
//
// 各役職は2系統の情報を持つ:
//  - systemPrompt: LLMに渡す人格・観点の指示（機械向け）。
//  - profile (specialty / perspective / whenToCall / stance):
//      画面で人間が読むためのプレーンな説明。役員紹介ポップアップに使う。
//
// プロンプト方針:
// - 役職ごとに人格と観点を明確に差別化し、課題によって対立軸が生まれるようにする。
// - マークダウン記号（**、#、-、1. など）は使わせない。会議の発言として自然な
//   プレーンな日本語の話し言葉で書かせる（表示側で記号レンダリングはしない）。

export interface RoleProfile {
  specialty: string // 専門領域
  perspective: string // 重視する観点
  whenToCall: string // どんな課題のときに招集すると良いか
  stance: string // 発言のスタンス（短い一言。例: 慎重・リスク重視）
}

export interface Role {
  id: string
  name: string // 表示名（短い識別名）
  title: string // 役職の正式名称
  systemPrompt: string // 人格・観点を定義するシステムプロンプト（LLM向け）
  accentColor: string // UI上の識別色（控えめに使う）
  profile: RoleProfile // 画面表示用のプレーンな説明
  isChair?: boolean // 議長（CEO）。常に固定参加・選択不可。
}

